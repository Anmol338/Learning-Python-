# Relational Operators
# ==, !=, >, >=, <, <=

num1 = int(input("Enter the first number you want tot check: \n"))
num2 = int(input("Enter the second number you want tot check: \n"))

# ==
check = num1 == num2
print(check)

check = num1 != num2
print(check)

check = num1 > num2
print(check)

check = num1 >= num2
print(check)

check = num1 < num2
print(check)

check = num1 <= num2
print(check)